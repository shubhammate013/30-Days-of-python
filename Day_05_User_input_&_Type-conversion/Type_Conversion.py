# In Python, you can convert data from one type to another using type conversion functions. Some commonly used type conversion functions include:

  # 1. int(): Converts a value to an integer.
  # 2. float(): Converts a value to a floating-point number.
  # 3. str(): Converts a value to a string.
  # 4. bool(): Converts a value to a Boolean.

# Convert to integer
num_string = "10"
num_integer = int(num_string)
print(num_integer)  # Output: 10

# Convert to float
num_float = float(num_string)
print(num_float)  # Output: 10.0

# Convert to string
num_str = str(num_float)
print(num_str)  # Output: '10.0'

# Convert to boolean
bool_value = bool(num_integer)
print(bool_value)  # Output: True if num_integer is not zero, False otherwise

